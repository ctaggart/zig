fn BF_decode(dst: [*]u32, src: [*]const u8, size: usize) c_int {
    var dptr: [*]u8 = @ptrCast(dst);
    const end: usize = @intFromPtr(dptr) + size;
    var sptr = src;

    while (true) {
        var tmp: u32 = @as(u32, sptr[0]) -% 0x20;
        sptr += 1;
        if (tmp >= 0x60) return -1;
        const c1 = BF_atoi64[tmp];
        if (c1 > 63) return -1;

        tmp = @as(u32, sptr[0]) -% 0x20;
        sptr += 1;
        if (tmp >= 0x60) return -1;
        const c2 = BF_atoi64[tmp];
        if (c2 > 63) return -1;

        dptr[0] = @intCast((c1 << 2) | ((c2 & 0x30) >> 4));
        dptr += 1;
        if (@intFromPtr(dptr) >= end) break;

        tmp = @as(u32, sptr[0]) -% 0x20;
        sptr += 1;
        if (tmp >= 0x60) return -1;
        const c3 = BF_atoi64[tmp];
        if (c3 > 63) return -1;

        dptr[0] = @intCast(((c2 & 0x0F) << 4) | ((c3 & 0x3C) >> 2));
        dptr += 1;
        if (@intFromPtr(dptr) >= end) break;

        tmp = @as(u32, sptr[0]) -% 0x20;
        sptr += 1;
        if (tmp >= 0x60) return -1;
        const c4 = BF_atoi64[tmp];
        if (c4 > 63) return -1;

        dptr[0] = @intCast(((c3 & 0x03) << 6) | c4);
        dptr += 1;
        if (@intFromPtr(dptr) >= end) break;
    }
    return 0;
}

fn BF_encode(dst: [*]u8, src: [*]const u32, size: usize) void {
    var sptr: [*]const u8 = @ptrCast(src);
    const end = @intFromPtr(sptr) + size;
    var dptr = dst;

    while (true) {
        const c1_byte = sptr[0];
        sptr += 1;
        var c1: u32 = c1_byte;
        dptr[0] = BF_itoa64[c1 >> 2];
        dptr += 1;
        c1 = (c1 & 0x03) << 4;
        if (@intFromPtr(sptr) >= end) {
            dptr[0] = BF_itoa64[c1];
            break;
        }
        const c2_byte = sptr[0];
        sptr += 1;
        var c2: u32 = c2_byte;
        c1 |= c2 >> 4;
        dptr[0] = BF_itoa64[c1];
        dptr += 1;
        c1 = (c2 & 0x0f) << 2;
        if (@intFromPtr(sptr) >= end) {
            dptr[0] = BF_itoa64[c1];
            break;
        }
        c2 = sptr[0];
        sptr += 1;
        c1 |= c2 >> 6;
        dptr[0] = BF_itoa64[c1];
        dptr += 1;
        dptr[0] = BF_itoa64[c2 & 0x3f];
        dptr += 1;
        if (@intFromPtr(sptr) >= end) break;
    }
}

fn BF_swap(x: [*]u32, count: usize) void {
    if (comptime builtin.cpu.arch.endian() == .little) {
        for (0..count) |i| x[i] = @byteSwap(x[i]);
    }
}

fn bfRound(ctx: *BF_ctx, L: u32, R: *u32, N: usize) void {
    const s3 = ctx.S[3][L & 0xFF];
    const s2 = ctx.S[2][(L >> 8) & 0xFF];
    var s1 = ctx.S[1][(L >> 16) & 0xFF];
    const s0 = ctx.S[0][L >> 24];
    s1 = s1 +% s0;
    s1 ^= s2;
    R.* ^= ctx.P[N + 1];
    s1 = s1 +% s3;
    R.* ^= s1;
}

fn BF_encrypt(ctx: *BF_ctx, L_in: u32, R_in: u32, start: [*]u32, end: [*]u32) u32 {
    var L = L_in;
    var R = R_in;
    var ptr = start;

    while (true) {
        L ^= ctx.P[0];
        var i: usize = 0;
        while (i < 16) : (i += 2) {
            bfRound(ctx, L, &R, i);
            bfRound(ctx, R, &L, i + 1);
        }
        const tmp = R;
        R = L;
        L = tmp ^ ctx.P[BF_N + 1];
        ptr[0] = L;
        ptr[1] = R;
        ptr += 2;
        if (@intFromPtr(ptr) >= @intFromPtr(end)) break;
    }
    return L;
}

fn BF_set_key(key: [*:0]const u8, expanded: *[BF_N + 2]u32, initial: *[BF_N + 2]u32, flags: u8) void {
    const bug: usize = flags & 1;
    const safety: u32 = (@as(u32, flags) & 2) << 15;
    var sign: u32 = 0;
    var diff: u32 = 0;
    var ptr: [*]const u8 = key;

    for (0..BF_N + 2) |i| {
        var tmp: [2]u32 = .{ 0, 0 };
        for (0..4) |j| {
            tmp[0] <<= 8;
            tmp[0] |= @as(u32, ptr[0]);
            tmp[1] <<= 8;
            const sc: i8 = @bitCast(ptr[0]);
            const sval: u32 = @bitCast(@as(i32, sc));
            tmp[1] |= sval;
            if (j != 0) sign |= tmp[1] & 0x80;
            if (ptr[0] == 0) {
                ptr = key;
            } else {
                ptr += 1;
            }
        }
        diff |= tmp[0] ^ tmp[1];
        expanded[i] = tmp[bug];
        initial[i] = BF_init_state.P[i] ^ tmp[bug];
    }

    diff |= diff >> 16;
    diff &= 0xffff;
    diff +%= 0xffff;
    sign <<= 9;
    sign &= ~diff & safety;
    initial[0] ^= sign;
}

fn BF_crypt(key: [*:0]const u8, setting: [*:0]const u8, output: [*]u8, min_count: u32) ?[*]u8 {
    if (setting[0] != '$' or setting[1] != '2') return null;
    const sub = setting[2];
    if (sub < 'a' or sub > 'z') return null;
    if (flags_by_subtype[sub - 'a'] == 0) return null;
    if (setting[3] != '$') return null;
    if (setting[4] -% '0' > 1) return null;
    if (setting[5] -% '0' > 9) return null;
    if (setting[6] != '$') return null;

    const cost: u32 = (@as(u32, setting[4] - '0')) * 10 + (setting[5] - '0');
    var count: u32 = @as(u32, 1) << @intCast(cost);
    if (count < min_count) return null;

    var ctx: BF_ctx = undefined;
    var expanded_key: [BF_N + 2]u32 = undefined;
    var salt: [4]u32 = undefined;
    var bin_out: [6]u32 = undefined;

    if (BF_decode(&salt, setting + 7, 16) != 0) return null;
    BF_swap(&salt, 4);

    BF_set_key(key, &expanded_key, &ctx.P, flags_by_subtype[sub - 'a']);
    ctx.S = BF_init_state.S;

    // Initial key schedule: encrypt salt into P and S
    {
        var L: u32 = 0;
        var R: u32 = 0;
        const ps_start: [*]u32 = @ptrCast(&ctx.P[0]);
        const ps_end: [*]u32 = ps_start + BF_PS_SIZE;
        var ps_ptr = ps_start;

        while (true) {
            L = BF_encrypt(&ctx, L ^ salt[0], R ^ salt[1], ps_ptr, ps_ptr);
            R = (ps_ptr + 1)[0];
            ps_ptr += 2;
            if (@intFromPtr(ps_ptr) >= @intFromPtr(ps_end)) break;

            L = BF_encrypt(&ctx, L ^ salt[2], R ^ salt[3], ps_ptr, ps_ptr);
            R = (ps_ptr + 1)[0];
            ps_ptr += 2;
        }
    }

    // Eksblowfish iterations
    while (count > 0) : (count -= 1) {
        // XOR P with expanded key
        var ei: usize = 0;
        while (ei < BF_N + 2) : (ei += 2) {
            ctx.P[ei] ^= expanded_key[ei];
            ctx.P[ei + 1] ^= expanded_key[ei + 1];
        }

        // Encrypt all with 0,0
        const ps_start: [*]u32 = @ptrCast(&ctx.P[0]);
        const ps_end: [*]u32 = ps_start + BF_PS_SIZE;
        _ = BF_encrypt(&ctx, 0, 0, ps_start, ps_end);

        // XOR P with salt
        var si: usize = 0;
        while (si < BF_N) : (si += 4) {
            ctx.P[si] ^= salt[0];
            ctx.P[si + 1] ^= salt[1];
            ctx.P[si + 2] ^= salt[2];
            ctx.P[si + 3] ^= salt[3];
        }
        ctx.P[16] ^= salt[0];
        ctx.P[17] ^= salt[1];

        // Encrypt all again
        _ = BF_encrypt(&ctx, 0, 0, ps_start, ps_end);
    }

    // Encrypt magic words 64 times each pair
    for (0..3) |pair| {
        const i = pair * 2;
        var LR: [2]u32 = .{ BF_magic_w[i], BF_magic_w[i + 1] };
        var cnt64: u32 = 64;
        while (cnt64 > 0) : (cnt64 -= 1) {
            const lr_ptr: [*]u32 = &LR;
            LR[0] = BF_encrypt(&ctx, LR[0], LR[1], lr_ptr, lr_ptr);
        }
        bin_out[i] = LR[0];
        bin_out[i + 1] = LR[1];
    }

    @memcpy(output[0 .. 7 + 22 - 1], setting[0 .. 7 + 22 - 1]);
    const last_salt_idx: u32 = @as(u32, setting[7 + 22 - 1]) -% 0x20;
    output[7 + 22 - 1] = if (last_salt_idx < 0x60) BF_itoa64[BF_atoi64[last_salt_idx] & 0x30] else BF_itoa64[0];

    BF_swap(&bin_out, 6);
    BF_encode(output + (7 + 22), &bin_out, 23);
    output[7 + 22 + 31] = 0;

    return output;
}

fn cryptBlowfish(key: [*:0]const u8, setting: [*:0]const u8, output: [*]u8) ?[*:0]u8 {
    const test_key = "8b \xd0\xc1\xd2\xcf\xcc\xd8";
    const test_setting_base = "$2a$00$abcdefghijklmnopqrstuu";
    const test_hashes: [2][34]u8 = .{
        "i1D709vfamulimlGcq0qq3UvuUasvEa\x00\x55\x00".*,
        "VUrPmXD6q/nVSSp7pNDhCR9071IfIRe\x00\x55\x00".*,
    };

    const retval = BF_crypt(key, setting, output, 16);

    var buf_s: [29:0]u8 = undefined;
    var buf_o: [63]u8 = undefined;
    @memcpy(&buf_s, test_setting_base);
    @memset(&buf_o, 0x55);
    buf_o[62] = 0;

    var which_hash: usize = 0;
    if (retval != null) {
        const fl = flags_by_subtype[setting[2] - 'a'];
        which_hash = fl & 1;
        buf_s[2] = setting[2];
    }

    const p = BF_crypt(test_key, &buf_s, &buf_o, 1);

    const ok = (p != null) and
        (@intFromPtr(p.?) == @intFromPtr(&buf_o)) and
        std.mem.eql(u8, buf_o[0 .. 7 + 22], buf_s[0 .. 7 + 22]) and
        std.mem.eql(u8, buf_o[7 + 22 .. 7 + 22 + 34], &test_hashes[which_hash]);

    // Additional key-schedule correctness test
    var ae: [BF_N + 2]u32 = undefined;
    var ai: [BF_N + 2]u32 = undefined;
    var ye: [BF_N + 2]u32 = undefined;
    var yi: [BF_N + 2]u32 = undefined;
    const k = "\xff\xa3" ++ "34" ++ "\xff\xff\xff\xa3" ++ "345";
    BF_set_key(k, &ae, &ai, 2);
    BF_set_key(k, &ye, &yi, 4);
    ai[0] ^= 0x10000;
    const ok2 = ok and
        ai[0] == 0xdb9c59bc and
        ye[17] == 0x33343500 and
        std.mem.eql(u32, &ae, &ye) and
        std.mem.eql(u32, &ai, &yi);

    if (ok2 and retval != null) return @ptrCast(retval.?);
    return @constCast(@ptrCast(@as(*const [1]u8, &"*"[0])));
}

// ======== setkey / encrypt (from encrypt.c) ========

fn setkeyFunc(key: [*]const u8) callconv(.c) void {
    var bkey: [8]u8 = undefined;
    var kp = key;
    for (0..8) |i| {
        bkey[i] = 0;
        for (0..8) |jr| {
            const j: u3 = @intCast(7 - jr);
            bkey[i] |= (kp[0] & 1) << j;
            kp += 1;
        }
    }
    desSetkey(&bkey, &encrypt_key);
}

fn encryptFunc(block: [*]u8, edflag: c_int) callconv(.c) void {
    var b: [2]u32 = .{ 0, 0 };
    var p = block;
    for (0..2) |i| {
        b[i] = 0;
        for (0..32) |jr| {
            const j: u5 = @intCast(31 - jr);
            b[i] |= @as(u32, p[0] & 1) << j;
            p += 1;
        }
    }

    var key_ptr: *ExpandedKey = &encrypt_key;
    var decrypt_key: ExpandedKey = undefined;
    if (edflag != 0) {
        for (0..16) |i| {
            decrypt_key.l[i] = encrypt_key.l[15 - i];
            decrypt_key.r[i] = encrypt_key.r[15 - i];
        }
        key_ptr = &decrypt_key;
    }

    doDes(b[0], b[1], &b[0], &b[1], 1, 0, key_ptr);

    p = block;
    for (0..2) |i| {
        for (0..32) |jr| {
            const j: u5 = @intCast(31 - jr);
            p[0] = @intCast((b[i] >> j) & 1);
            p += 1;
        }
    }
}

// ======== Top-level crypt ========

fn cryptRFunc(key: [*:0]const u8, salt: [*:0]const u8, data: [*]u8) callconv(.c) ?[*:0]u8 {
    const output = data;
    if (salt[0] == '$' and salt[1] != 0 and salt[2] != 0) {
        if (salt[1] == '1' and salt[2] == '$')
            return cryptMd5(key, salt, output);
        if (salt[1] == '2' and salt[3] == '$')
            return cryptBlowfish(key, salt, output);
        if (salt[1] == '5' and salt[2] == '$')
            return cryptSha256(key, salt, output);
        if (salt[1] == '6' and salt[2] == '$')
            return cryptSha512(key, salt, output);
    }
    return cryptDes(key, salt, output);
}

fn cryptFunc(key: [*:0]const u8, salt: [*:0]const u8) callconv(.c) ?[*:0]u8 {
    return cryptRFunc(key, salt, &crypt_buf);
}
