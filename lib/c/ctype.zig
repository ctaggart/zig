    if (wc -% 0xfffc > 0x10ffff - 0xfffc or (wc & 0xfffe) == 0xfffe)
        return 0;
    return 1;
}

fn iswgraph_fn(wc: wint_t) callconv(.c) c_int {
    return @intFromBool(iswspace_fn(wc) == 0 and iswprint_fn(wc) != 0);
}

fn iswlower_fn(wc: wint_t) callconv(.c) c_int {
    return @intFromBool(towupper_fn(wc) != wc);
}

fn iswupper_fn(wc: wint_t) callconv(.c) c_int {
    return @intFromBool(towlower_fn(wc) != wc);
}

fn iswxdigit_fn(wc: wint_t) callconv(.c) c_int {
    return @intFromBool(wc -% '0' < 10 or (wc | 32) -% 'a' < 6);
}

fn iswctype_fn(wc: wint_t, t: wctype_t) callconv(.c) c_int {
    return switch (t) {
        1 => iswalnum_fn(wc),
        2 => iswalpha_fn(wc),
        3 => iswblank_fn(wc),
        4 => iswcntrl_fn(wc),
        5 => iswdigit_fn(wc),
        6 => iswgraph_fn(wc),
        7 => iswlower_fn(wc),
        8 => iswprint_fn(wc),
        9 => iswpunct_fn(wc),
        10 => iswspace_fn(wc),
        11 => iswupper_fn(wc),
        12 => iswxdigit_fn(wc),
        else => 0,
    };
}

fn wctype_fn(s: [*:0]const u8) callconv(.c) wctype_t {
    const names = [_][]const u8{
        "alnum", "alpha", "blank", "cntrl", "digit", "graph",
        "lower", "print", "punct", "space", "upper", "xdigit",
    };
    const name = std.mem.sliceTo(s, 0);
    for (names, 1..) |n, i| {
        if (std.mem.eql(u8, name, n)) return i;
    }
    return 0;
}

fn casemap(c_arg: c_uint, dir: u1) c_uint {
    const c0: c_uint = c_arg;
    if (c_arg >= 0x20000) return c_arg;

    const b: c_uint = c_arg >> 8;
    const c: c_uint = c_arg & 255;
    const x: c_uint = c / 3;
    const y: c_uint = c % 3;

    // Lookup entry in two-level base-6 table
    const v_raw: c_uint = casemap_tab[casemap_tab[b] *% 86 +% x];
    const mt = [_]c_uint{ 2048, 342, 57 };
    const v: c_uint = (v_raw *% mt[y] >> 11) % 6;

    // Use the bit vector out of the tables as an index into
    // a block-specific set of rules and decode the rule into
    // a type and a case-mapping delta.
    const r: i32 = casemap_rules[casemap_rulebases[b] +% v];
    const rt: c_uint = @bitCast(r & 255);
    const rd: i32 = r >> 8;

    // Rules 0/1 are simple lower/upper case with a delta.
    if (rt < 2) {
        const xor_val: i32 = @bitCast(rt ^ @as(c_uint, dir));
        const mask: i32 = -%xor_val;
        const delta: u32 = @bitCast(rd & mask);
        return c0 +% delta;
    }

    // Binary search for exceptions.
    var xn: c_uint = @bitCast(rd & 0xff);
    var xb: c_uint = @as(c_uint, @bitCast(rd)) >> 8;
    while (xn != 0) {
        const probe: c_uint = casemap_exceptions[xb +% xn / 2][0];
        if (probe == c) {
            const r2: i32 = casemap_rules[casemap_exceptions[xb +% xn / 2][1]];
            const rt2: c_uint = @bitCast(r2 & 255);
            const rd2: i32 = r2 >> 8;
            if (rt2 < 2) {
                const xor_val2: i32 = @bitCast(rt2 ^ @as(c_uint, dir));
                const mask2: i32 = -%xor_val2;
                const delta2: u32 = @bitCast(rd2 & mask2);
                return c0 +% delta2;
            }
            // Hard-coded for the four exceptional titlecase
            const tc_delta: u32 = if (dir != 0) @bitCast(@as(i32, -1)) else 1;
            return c0 +% tc_delta;
        } else if (probe > c) {
            xn /= 2;
        } else {
            xb += xn / 2;
            xn -= xn / 2;
        }
    }
    return c0;
}

fn towupper_fn(wc: wint_t) callconv(.c) wint_t {
    return casemap(wc, 1);
}

fn towlower_fn(wc: wint_t) callconv(.c) wint_t {
    return casemap(wc, 0);
}

fn towctrans_fn(wc: wint_t, trans: wctrans_t) callconv(.c) wint_t {
    if (trans == 1) return towupper_fn(wc);
    if (trans == 2) return towlower_fn(wc);
    return wc;
}

fn wctrans_fn(s: [*:0]const u8) callconv(.c) wctrans_t {
    const name = std.mem.sliceTo(s, 0);
    if (std.mem.eql(u8, name, "toupper")) return 1;
    if (std.mem.eql(u8, name, "tolower")) return 2;
    return 0;
}

fn wcwidth_fn(wc: wchar_t) callconv(.c) c_int {
    const uwc: c_uint = @bitCast(wc);
    if (uwc < 0xff)
        return if ((uwc +% 1 & 0x7f) >= 0x21) 1 else if (uwc != 0) @as(c_int, -1) else 0;
    if ((uwc & 0xfffeFFFF) < 0xfffe) {
        if (tableLookup(&nonspacing_table, uwc)) return 0;
        if (tableLookup(&wide_table, uwc)) return 2;
        return 1;
    }
    if ((uwc & 0xfffe) == 0xfffe) return -1;
    if (uwc -% 0x20000 < 0x20000) return 2;
    if (uwc == 0xe0001 or uwc -% 0xe0020 < 0x5f or uwc -% 0xe0100 < 0xef) return 0;
    return 1;
}

fn wcswidth_fn(wcs: [*]const wchar_t, n_arg: usize) callconv(.c) c_int {
    var l: c_int = 0;
    var k: c_int = 0;
    var n = n_arg;
    var p = wcs;
    while (n != 0) : ({
        n -= 1;
        p += 1;
    }) {
        if (p[0] == 0) break;
        k = wcwidth_fn(p[0]);
        if (k < 0) break;
        l += k;
    }
    return if (k < 0) k else l;
}

