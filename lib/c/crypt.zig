}

// ======== SHA256 crypt ========

fn hashmdSha256(h: *std.crypto.hash.sha2.Sha256, n: usize, md: *const [32]u8) void {
    var remaining = n;
    while (remaining > 32) : (remaining -= 32) h.update(md);
    h.update(md[0..remaining]);
}

fn sha256crypt(key: [*:0]const u8, setting: [*:0]const u8, output: [*]u8) ?[*]u8 {
    var md: [32]u8 = undefined;
    var kmd: [32]u8 = undefined;
    var smd: [32]u8 = undefined;

    const klen = strnlen(key, 257);
    if (klen > 256) return null;

    if (setting[0] != '$' or setting[1] != '5' or setting[2] != '$') return null;
    var salt: [*:0]const u8 = setting + 3;

    var rounds: u32 = 5000;
    var custom_rounds = false;
    if (salt[0] == 'r' and salt[1] == 'o' and salt[2] == 'u' and salt[3] == 'n' and
        salt[4] == 'd' and salt[5] == 's' and salt[6] == '=')
    {
        salt += 7;
        if (salt[0] < '0' or salt[0] > '9') return null;
        var u: u64 = 0;
        while (salt[0] >= '0' and salt[0] <= '9') : (salt += 1) {
            u = u * 10 + (salt[0] - '0');
            if (u > 9999999) u = 10000000;
        }
        if (salt[0] != '$') return null;
        salt += 1;
        if (u > 9999999) return null;
        if (u < 1000) rounds = 1000 else rounds = @intCast(u);
        custom_rounds = true;
    }

    var slen: usize = 0;
    while (slen < 16 and salt[slen] != 0 and salt[slen] != '$') : (slen += 1) {
        if (salt[slen] == '\n' or salt[slen] == ':') return null;
    }

    // B = sha(key salt key)
    var ctx = std.crypto.hash.sha2.Sha256.init(.{});
    ctx.update(key[0..klen]);
    ctx.update(salt[0..slen]);
    ctx.update(key[0..klen]);
    ctx.final(&md);

    // A = sha(key salt repeat-B alternate-B-key)
    var ctx2 = std.crypto.hash.sha2.Sha256.init(.{});
    ctx2.update(key[0..klen]);
    ctx2.update(salt[0..slen]);
    hashmdSha256(&ctx2, klen, &md);
    var i: usize = klen;
    while (i > 0) : (i >>= 1) {
        if (i & 1 != 0) ctx2.update(&md) else ctx2.update(key[0..klen]);
    }
    ctx2.final(&md);

    // DP = sha(repeat-key)
    var ctx3 = std.crypto.hash.sha2.Sha256.init(.{});
    for (0..klen) |_| ctx3.update(key[0..klen]);
    ctx3.final(&kmd);

    // DS = sha(repeat-salt)
    var ctx4 = std.crypto.hash.sha2.Sha256.init(.{});
    for (0..(16 + @as(usize, md[0]))) |_| ctx4.update(salt[0..slen]);
    ctx4.final(&smd);

    // iterate
    for (0..rounds) |it| {
        var ctx5 = std.crypto.hash.sha2.Sha256.init(.{});
        if (it % 2 != 0) hashmdSha256(&ctx5, klen, &kmd) else ctx5.update(&md);
        if (it % 3 != 0) ctx5.update(smd[0..slen]);
        if (it % 7 != 0) hashmdSha256(&ctx5, klen, &kmd);
        if (it % 2 != 0) ctx5.update(&md) else hashmdSha256(&ctx5, klen, &kmd);
        ctx5.final(&md);
    }

    // build output prefix
    var pos: usize = 0;
    output[pos] = '$'; pos += 1;
    output[pos] = '5'; pos += 1;
    output[pos] = '$'; pos += 1;
    if (custom_rounds) {
        const rprefix = "rounds=";
        @memcpy(output[pos .. pos + 7], rprefix);
        pos += 7;
        pos += writeU32Decimal(output[pos..], rounds);
        output[pos] = '$'; pos += 1;
    }
    @memcpy(output[pos .. pos + slen], salt[0..slen]);
    pos += slen;
    output[pos] = '$'; pos += 1;

    var p = output + pos;
    const perm = [10][3]u8{
        .{ 0, 10, 20 }, .{ 21, 1, 11 }, .{ 12, 22, 2 },  .{ 3, 13, 23 }, .{ 24, 4, 14 },
        .{ 15, 25, 5 }, .{ 6, 16, 26 }, .{ 27, 7, 17 },  .{ 18, 28, 8 }, .{ 9, 19, 29 },
    };
    for (perm) |row| {
        p = to64(p, (@as(u32, md[row[0]]) << 16) | (@as(u32, md[row[1]]) << 8) | md[row[2]], 4);
    }
    p = to64(p, (@as(u32, md[31]) << 8) | md[30], 3);
    p[0] = 0;
    return output;
}

