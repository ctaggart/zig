        retval = sch - ('A' - 12);
        if (sch >= 'a') retval = sch - ('a' - 38);
    }
    return @intCast(retval & 0x3f);
}

fn ascii_is_unsafe(ch: u8) bool {
    return ch == 0 or ch == '\n' or ch == ':';
}

fn setup_salt(salt: u32) u32 {
    var saltbits: u32 = 0;
    var saltbit: u32 = 1;
    var obit: u32 = 0x800000;
    for (0..24) |_| {
        if (salt & saltbit != 0) saltbits |= obit;
        saltbit <<= 1;
        obit >>= 1;
    }
    return saltbits;
}

fn desSetkey(key: []const u8, ekey: *ExpandedKey) void {
    const rawkey0: u32 = (@as(u32, key[3])) |
        (@as(u32, key[2]) << 8) |
        (@as(u32, key[1]) << 16) |
        (@as(u32, key[0]) << 24);
    const rawkey1: u32 = (@as(u32, key[7])) |
        (@as(u32, key[6]) << 8) |
        (@as(u32, key[5]) << 16) |
        (@as(u32, key[4]) << 24);

    var k0: u32 = 0;
    var k1: u32 = 0;
    var ibit: u32 = 28;
    for (0..4) |i| {
        const j = i << 1;
        k0 |= key_perm_maskl[i][@intCast((rawkey0 >> @as(u5, @intCast(ibit))) & 0xf)] |
              key_perm_maskl[i + 4][@intCast((rawkey1 >> @as(u5, @intCast(ibit))) & 0xf)];
        k1 |= key_perm_maskr[j][@intCast((rawkey0 >> @as(u5, @intCast(ibit))) & 0xf)];
        ibit -= 4;
        k1 |= key_perm_maskr[j + 1][@intCast((rawkey0 >> @as(u5, @intCast(ibit))) & 0xf)] |
              key_perm_maskr[i + 8][@intCast((rawkey1 >> @as(u5, @intCast(ibit))) & 0xf)];
        if (i < 3) ibit -= 4;
    }

    var shifts: u32 = 0;
    for (0..16) |round| {
        shifts +%= key_shifts[round];
        const t0 = (k0 << @intCast(shifts)) | (k0 >> @intCast(28 - shifts));
        const t1 = (k1 << @intCast(shifts)) | (k1 >> @intCast(28 - shifts));
        var kl: u32 = 0;
        var kr: u32 = 0;
        var ib: u32 = 25;
        for (0..4) |i| {
            kl |= comp_maskl0[i][@intCast((t0 >> @as(u5, @intCast(ib))) & 7)];
            kr |= comp_maskr0[i][@intCast((t1 >> @as(u5, @intCast(ib))) & 7)];
            ib -= 4;
            kl |= comp_maskl1[i][@intCast((t0 >> @as(u5, @intCast(ib))) & 0xf)];
            kr |= comp_maskr1[i][@intCast((t1 >> @as(u5, @intCast(ib))) & 0xf)];
            if (i < 3) ib -= 3;
        }
        ekey.l[round] = kl;
        ekey.r[round] = kr;
    }
}

fn doDes(l_in: u32, r_in: u32, l_out: *u32, r_out: *u32, count: u32, saltbits: u32, ekey: *const ExpandedKey) void {
    var l: u32 = 0;
    var r: u32 = 0;
    if (l_in | r_in != 0) {
        var ibit: u32 = 28;
        for (0..8) |i| {
            l |= ip_maskl[i][@intCast((l_in >> @as(u5, @intCast(ibit))) & 0xf)] |
                 ip_maskl[i + 8][@intCast((r_in >> @as(u5, @intCast(ibit))) & 0xf)];
            r |= ip_maskr[i][@intCast((l_in >> @as(u5, @intCast(ibit))) & 0xf)] |
                 ip_maskr[i + 8][@intCast((r_in >> @as(u5, @intCast(ibit))) & 0xf)];
            if (i < 7) ibit -= 4;
        }
    }

    var cnt = count;
    var f: u32 = 0;
    while (cnt > 0) : (cnt -= 1) {
        var round: usize = 16;
        var kl_idx: usize = 0;
        var kr_idx: usize = 0;
        while (round > 0) : (round -= 1) {
            const r48l: u32 = ((r & 0x00000001) << 23) |
                ((r & 0xf8000000) >> 9) |
                ((r & 0x1f800000) >> 11) |
                ((r & 0x01f80000) >> 13) |
                ((r & 0x001f8000) >> 15);
            const r48r: u32 = ((r & 0x0001f800) << 7) |
                ((r & 0x00001f80) << 5) |
                ((r & 0x000001f8) << 3) |
                ((r & 0x0000001f) << 1) |
                ((r & 0x80000000) >> 31);
            const ftmp = (r48l ^ r48r) & saltbits;
            const xr48l = r48l ^ ftmp ^ ekey.l[kl_idx];
            const xr48r = r48r ^ ftmp ^ ekey.r[kr_idx];
            kl_idx += 1;
            kr_idx += 1;
            f = psbox[0][@intCast(xr48l >> 18)] |
                psbox[1][@intCast((xr48l >> 12) & 0x3f)] |
                psbox[2][@intCast((xr48l >> 6) & 0x3f)] |
                psbox[3][@intCast(xr48l & 0x3f)] |
                psbox[4][@intCast(xr48r >> 18)] |
                psbox[5][@intCast((xr48r >> 12) & 0x3f)] |
                psbox[6][@intCast((xr48r >> 6) & 0x3f)] |
                psbox[7][@intCast(xr48r & 0x3f)];
            f ^= l;
            l = r;
            r = f;
        }
        r = l;
        l = f;
    }

    var ibit: u32 = 28;
    var lo: u32 = 0;
    var ro: u32 = 0;
    for (0..4) |i| {
        ro |= fp_maskr[i][@intCast((l >> @as(u5, @intCast(ibit))) & 0xf)] |
              fp_maskr[i + 4][@intCast((r >> @as(u5, @intCast(ibit))) & 0xf)];
        ibit -= 4;
        lo |= fp_maskl[i][@intCast((l >> @as(u5, @intCast(ibit))) & 0xf)] |
              fp_maskl[i + 4][@intCast((r >> @as(u5, @intCast(ibit))) & 0xf)];
        if (i < 3) ibit -= 4;
    }
    l_out.* = lo;
    r_out.* = ro;
}

fn desCipher(in: []const u8, out: []u8, count: u32, saltbits: u32, ekey: *const ExpandedKey) void {
    const rawl: u32 = (@as(u32, in[3])) | (@as(u32, in[2]) << 8) | (@as(u32, in[1]) << 16) | (@as(u32, in[0]) << 24);
    const rawr: u32 = (@as(u32, in[7])) | (@as(u32, in[6]) << 8) | (@as(u32, in[5]) << 16) | (@as(u32, in[4]) << 24);
    var l_out: u32 = undefined;
    var r_out: u32 = undefined;
    doDes(rawl, rawr, &l_out, &r_out, count, saltbits, ekey);
    out[0] = @intCast(l_out >> 24);
    out[1] = @intCast(l_out >> 16);
    out[2] = @intCast(l_out >> 8);
    out[3] = @intCast(l_out);
    out[4] = @intCast(r_out >> 24);
    out[5] = @intCast(r_out >> 16);
    out[6] = @intCast(r_out >> 8);
    out[7] = @intCast(r_out);
}

fn cryptExtendedRUut(key_z: [*:0]const u8, setting_z: [*:0]const u8, output: [*]u8) ?[*]u8 {
    const key: [*]const u8 = key_z;
    const setting: [*]const u8 = setting_z;
    var ekey: ExpandedKey = undefined;
    var keybuf: [8]u8 = undefined;
    var key_ptr: [*]const u8 = key;

    for (0..8) |qi| {
        keybuf[qi] = key_ptr[0] << 1;
        if (key_ptr[0] != 0) key_ptr += 1;
    }
    desSetkey(&keybuf, &ekey);

    var count: u32 = 0;
    var salt: u32 = 0;
    var p: [*]u8 = undefined;

    if (setting[0] == '_') {
        // new-style: underscore + 4 count chars + 4 salt chars
        for (1..5) |i| {
            const value = ascii_to_bin(setting[i]);
            if (ascii64[value] != setting[i]) return null;
            count |= value << @intCast((i - 1) * 6);
        }
        if (count == 0) return null;
        for (5..9) |i| {
            const value = ascii_to_bin(setting[i]);
            if (ascii64[value] != setting[i]) return null;
            salt |= value << @intCast((i - 5) * 6);
        }
        while (key_ptr[0] != 0) {
            desCipher(&keybuf, &keybuf, 1, 0, &ekey);
            var q: usize = 0;
            while (q < 8 and key_ptr[0] != 0) : (q += 1) {
                keybuf[q] ^= key_ptr[0] << 1;
                key_ptr += 1;
            }
            desSetkey(&keybuf, &ekey);
        }
        @memcpy(output[0..9], setting[0..9]);
        output[9] = 0;
        p = output + 9;
    } else {
        // old-style: 2 salt chars
        count = 25;
        if (ascii_is_unsafe(setting[0]) or ascii_is_unsafe(setting[1])) return null;
        salt = (ascii_to_bin(setting[1]) << 6) | ascii_to_bin(setting[0]);
        output[0] = setting[0];
        output[1] = setting[1];
        p = output + 2;
    }

    var r0: u32 = undefined;
    var r1: u32 = undefined;
    doDes(0, 0, &r0, &r1, count, setup_salt(salt), &ekey);

    const l1 = r0 >> 8;
    p[0] = ascii64[(l1 >> 18) & 0x3f];
    p[1] = ascii64[(l1 >> 12) & 0x3f];
    p[2] = ascii64[(l1 >> 6) & 0x3f];
    p[3] = ascii64[l1 & 0x3f];
    p += 4;
    const l2 = (r0 << 16) | ((r1 >> 16) & 0xffff);
    p[0] = ascii64[(l2 >> 18) & 0x3f];
    p[1] = ascii64[(l2 >> 12) & 0x3f];
    p[2] = ascii64[(l2 >> 6) & 0x3f];
    p[3] = ascii64[l2 & 0x3f];
    p += 4;
    const l3 = r1 << 2;
    p[0] = ascii64[(l3 >> 12) & 0x3f];
    p[1] = ascii64[(l3 >> 6) & 0x3f];
    p[2] = ascii64[l3 & 0x3f];
    p[3] = 0;
    return output;
}

fn cryptDes(key: [*:0]const u8, setting: [*:0]const u8, output: [*]u8) ?[*:0]u8 {
    const test_key = "\x80\xff\x80\x01 \x7f\x81\x80\x80\x0d\x0a\xff\x7f \x81 test";
    var test_setting: [*:0]const u8 = "_0.../9Zz";
    var test_hash: []const u8 = "_0.../9ZzX7iSJNd21sU";
    var test_buf: [21]u8 = undefined;

    if (setting[0] != '_') {
        test_setting = "\x80x";
        test_hash = "\x80x22/wK52ZKGA";
    }

    const retval = cryptExtendedRUut(key, setting, output);
    const p = cryptExtendedRUut(test_key, test_setting, &test_buf);
    if (p != null and cstrsEql(p.?, test_hash) and retval != null)
        return @ptrCast(retval.?);
    return @constCast(@ptrCast(if (setting[0] == '*') "x" else "*"));
}

