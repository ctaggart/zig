fn iswalpha_fn(wc: wint_t) callconv(.c) c_int {
    if (wc < 0x20000) return @intFromBool(tableLookup(&alpha_table, wc));
    if (wc < 0x2fffe) return 1;
    return 0;
}

fn iswpunct_fn(wc: wint_t) callconv(.c) c_int {
    if (wc < 0x20000) return @intFromBool(tableLookup(&punct_table, wc));
    return 0;
}

fn iswdigit_fn(wc: wint_t) callconv(.c) c_int {
    return @intFromBool(wc -% '0' < 10);
}

fn iswalnum_fn(wc: wint_t) callconv(.c) c_int {
    return @intFromBool(iswdigit_fn(wc) != 0 or iswalpha_fn(wc) != 0);
}

fn iswblank_fn(wc: wint_t) callconv(.c) c_int {
    return @intFromBool(wc == ' ' or wc == '\t');
}

fn iswcntrl_fn(wc: wint_t) callconv(.c) c_int {
