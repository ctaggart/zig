fn strnlen(s: [*:0]const u8, maxlen: usize) usize {
    var i: usize = 0;
    while (i < maxlen and s[i] != 0) : (i += 1) {}
    return i;
}

fn cstrsEql(a: [*]const u8, b: []const u8) bool {
    var i: usize = 0;
    while (i < b.len and a[i] != 0) : (i += 1) {
        if (a[i] != b[i]) return false;
    }
    return i == b.len and a[i] == 0;
}

const b64 = "./0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";

fn to64(s_init: [*]u8, u_init: u32, n_init: i32) [*]u8 {
    var s = s_init;
    var u = u_init;
    var n = n_init;
    while (n > 0) : (n -= 1) {
        s[0] = b64[u % 64];
        s += 1;
        u /= 64;
    }
    return s;
}

fn writeU32Decimal(buf: [*]u8, val: u32) usize {
    if (val == 0) {
        buf[0] = '0';
        return 1;
    }
    var tmp: [10]u8 = undefined;
    var len: usize = 0;
    var v = val;
    while (v > 0) : (v /= 10) {
        tmp[len] = @intCast('0' + v % 10);
        len += 1;
    }
    for (0..len) |i| buf[i] = tmp[len - 1 - i];
    return len;
}

// ======== MD5 crypt ========

fn md5crypt(key: [*:0]const u8, setting: [*:0]const u8, output: [*]u8) ?[*]u8 {
    var md: [16]u8 = undefined;
    var md2: [16]u8 = undefined;

    const klen = strnlen(key, 30001);
    if (klen > 30000) return null;

    if (setting[0] != '$' or setting[1] != '1' or setting[2] != '$') return null;
    const salt = setting + 3;
    var slen: usize = 0;
    while (slen < 8 and salt[slen] != 0 and salt[slen] != '$') : (slen += 1) {}

    // md5(key salt key)
    var ctx = std.crypto.hash.Md5.init(.{});
    ctx.update(key[0..klen]);
    ctx.update(salt[0..slen]);
    ctx.update(key[0..klen]);
    ctx.final(&md);

    // md5(key "$1$" salt repeated-md weird-key[0]-0)
    var ctx2 = std.crypto.hash.Md5.init(.{});
    ctx2.update(key[0..klen]);
    ctx2.update(setting[0 .. 3 + slen]);
    var i: usize = klen;
    while (i > 16) : (i -= 16) ctx2.update(&md);
    ctx2.update(md[0..i]);
    md[0] = 0;
    i = klen;
    while (i != 0) : (i >>= 1) {
        if (i & 1 != 0) ctx2.update(md[0..1]) else ctx2.update(key[0..1]);
    }
    ctx2.final(&md);

    // 1000 iterations
    for (0..1000) |it| {
        var ctx3 = std.crypto.hash.Md5.init(.{});
        if (it % 2 != 0) ctx3.update(key[0..klen]) else ctx3.update(&md);
        if (it % 3 != 0) ctx3.update(salt[0..slen]);
        if (it % 7 != 0) ctx3.update(key[0..klen]);
        if (it % 2 != 0) ctx3.update(&md) else ctx3.update(key[0..klen]);
        ctx3.final(&md2);
        md = md2;
    }

    @memcpy(output[0 .. 3 + slen], setting[0 .. 3 + slen]);
    var p = output + 3 + slen;
    p[0] = '$';
    p += 1;
    const perm = [5][3]u8{
        .{ 0, 6, 12 }, .{ 1, 7, 13 }, .{ 2, 8, 14 }, .{ 3, 9, 15 }, .{ 4, 10, 5 },
    };
    for (perm) |row| {
        p = to64(p, (@as(u32, md[row[0]]) << 16) | (@as(u32, md[row[1]]) << 8) | md[row[2]], 4);
    }
    p = to64(p, md[11], 2);
    p[0] = 0;
    return output;
}

